<h1>Notre mission</h1>
<div class="row">
    <div class="col-sm-5">
        <img src="pictures/mission1.jpg" alt="mission1 "/>
        <p>
            Ce <strong>voyage humanitaire</strong> d’un mois a pour premier but de promouvoir la <strong>psychomotricité</strong> à l’international, que ce soit auprès des adultes ou auprès des enfants.
        </p>

        <p>
            En effet, nous aimerions sensibiliser au <strong>développement psychomoteur normal</strong> de l’enfant mais également aux difficultés qu’ils pourraient rencontrer, qu’elles soient cognitives, motrices ou psychologiques : déficience intellectuelle, dyspraxie, dysorthographie…
        </p>

        <p>
            Ce sera l’occasion de pouvoir proposer aux enfants <strong>différentes activités peu communes</strong> : relaxation par le toucher, la voix, les tissus ; l’enveloppement ; le toucher thérapeutique (massage) par des ballons, par des percussions osseuses mais aussi des activités plus ludiques comme des jeux de latéralité, des parcours faisant travailler l’équilibre, les réflexes, le tonus etc..
        </p>
    </div>

    <div class="col-sm-2">
    </div>

    <div class="col-sm-5">
        <img src="pictures/mission2.jpg" alt="mission2 "/>
        <p>
            Nous ferons également du <strong>soutien scolaire</strong> d’une heure quotidiennement afin de maintenir leurs capacités et leurs acquis même pendant les vacances.
        </p>

        <p>
            Ce <strong>soutien humain</strong> sera épaulé d’un soutien matériel. En effet nous prévoyons de ramener des cahiers et des sacs afin d’équiper correctement ces petits élèves en quête de connaissance.
        </p>

        <p>
            De plus nous aimerions vraiment apporter une aide financière afin de pouvoir acheter de la peinture pour <strong>recouvrir de couleurs, de dessins et de gaieté</strong> les murs de l’hôpital pédiatrique qui vient d’être bâti à <strong>Diofior</strong>.
        </p>

        <p>
            Enfin, l’<strong>échange culturel</strong> sera au cœur de notre projet avec l’échange de pratiques, la découverte d’un mode de vie, de nourriture, de nouveaux paysages.
        </p>

        <p>
            Saurons-nous nous adapter à cet environnement tout nouveau pour nous ?
        </p>
    </div>
</div>
