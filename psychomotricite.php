<h1>Psychomotricité</h1>

<div class="row">
    <div class="col-sm-5">
        <img src="pictures/psychomotricite.png" class="img-responsive" alt="psychomotricite" />

        <p>
            La <strong>psychomotricité</strong> est à l’interface des dimensions corporelles et psychologiques.
        </p>

        <p>
            C’est une <strong>pratique thérapeutique</strong> destinée aux nouveaux-nés, aux enfants, adolescents, adultes, personnes-âgées… soit tout type de population ayant des difficultés motrices, psychologiques, cognitives, relationnelles…
        </p>

        <p>
            Le <strong>soin psychomoteur</strong> relève aussi bien de l’éducation psychomotrice, de la rééducation que de la thérapie.
        </p>

        <p>
            Il est important de savoir que le psychomotricien travaille seulement <strong>sous prescription médicale</strong>.
        </p>

        <p>
            Il commence souvent par établir un <strong>bilan psychomoteur du patient</strong> pour visualiser ses capacités et ses difficultés.
        </p>
    </div>

    <div class="col-sm-2"></div>

    <div class="col-sm-5">
        <img src="pictures/atelier_psychomotricite.jpg" class="img-responsive" alt="psychomotricite" />

        <p>
            De nombreux tests officiels existent en psychomotricité et permettent d’évaluer de nombreuses fonctions psychomotrices : équilibre, latéralité, motricité fine ou globale, tonus, repères spatio-temportels, schéma du corps, image du corps...
        </p>

        <p>
            Le psychomotricien est un <strong>thérapeute</strong> qui rééquilibre l’harmonie entre le corps et l’esprit du patient.<br />
            Il entreprend des prises en charge en prenant en compte le patient dans sa globalité.
        </p>

        <p>
            Il utilise de nombreuses médiations comme <strong>le jeu, la relaxation, la danse, les stimulations sensorielles, le théâtre</strong> et encore bien d’autres.<br />
            Le choix de la médiation est fait en fonction des attirances du patient et de son projet thérapeutique.
        </p>
    </div>
</div>
