<h1>Sénégal</h1>

<div id="senegal_phone">
    <h2>Géographie</h2>

    <p>
        Le village de Diofior est situé à l'Ouest du Sénégal, dans la région naturelle du Sine Saloum.<br />
        Juridiquement, la commune relève du département et de la région de Fatick.
    </p>

    <h2>Histoire</h2>

    <p>
        Selon les témoignages des anciens, Diofior auré été fondé au XIIIème siècle par Ndongo NIANE. Son histoire est indissociable de celle de Faoye, un village voisin.<br />
        En effet,  des populations sèreres de Faoye à la recherche de terres agricoles ont émigré vers Diofior. Mais suite à une épidémie de peste, le village fut décimé et refondé au XIXème siècle par Diombo THIANG.<br />
        En 1925, toutes les concessions se sont regroupées sur le site actuel pour former le village de Diofior qui signifie entente et amitié et fut érigée en commune en 1990.
    </p>

    <h2>Economie</h2>

    <p>
        Les activités économiques principales sont l’agriculture, l’élevage et la pêche. Les principales cultures sont l’arachide, l’arboriculture fruitière, le maïs et le mil.<br />
        En effet, la région de Fatick est située dans le bassin arachidier sénégalier et Diofior est localisé au niveau d'une zone maritime et fluviale.<br />
        La pêche constitue l'activité principale étant donné la perte de fertilité de certaines terres et l'infertilité d'autres, ainsi que l’absence d’un secteur tertiaire dynamique.
    </p>

    <h2>Démographie</h2>

    <p>
        Selon le dernier recensement et le taux de croissance de Diofior, on peut estimer sa population à 14000 habitants, avec une légère majorité de femmes.
    </p>

    <h2>Espoirs enfants Diofior</h2>

    <p>
        L'association <strong>espoirs enfants Diofior</strong> a pour objectif de contribuer au bien-être ce ses habitants et au développement de Diofior, notamment grâce à l'ouverture d'une école maternelle en 2008, le raccordement des maisons du village et des villages voisins à l'électricité en collaboration avec l'association "Y a de lélectricité dans l'air".<br />
        Le dernier projet en date est l'ouverture d'un centre pédiatrique et maternel, auquel a participé l'association "Psychomot' solidaires" en 2014 et 2015.<br />
        C'est avec cette association que nous nous sommes rattachées dans le but de préciser nos objectifs et pouvoir intervenir dans de meilleures conditions.<br />
        <b>Site de l'association</b> : <a href="http://www.espoirenfantsdiofior.com">Espoir enfants Diofor</a>
    </p>

    <h2>Psychomot solidaires</h2>

    <p>
        L'association <strong>Psychomot solidaires</strong>, créée en 2013 par un groupe d'étudiantes en psychomotricité de l'IFP de Lille a aidé à la scolarisation d'enfants vulnérables avec Espoirs enfants Diofior, entrepris de récolter des fonds afin d'apporter du matériel scolaire mais également ludique dans le but de créer une aire de jeux et de mettre en place des ateliers psychomoteurs.<br />
        Parmi les activités proposées, on trouve notamment un livre sensoriel réalisé par les enfants avec divers matériaux, où chacun peut raconter ce qu'il vit et ressent.<br />
        Ce livre constitue donc, en plus d'une activité de création et une stimulation sensorielle, un mode de communication interculturelle riche de sens.<br />
        Nous avons donc pris contact avec cette association afin de reprendre leur projet tout en établissant de nouveaux objectifs, tels que l'élaboration d'un "twister-scrabble".
    </p>
</div>

